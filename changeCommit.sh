OLD_EMAIL="eguerrero@etsy.com"
CORRECT_NAME="Erick Guerrero"
CORRECT_EMAIL="magnus_22_10@hotmail.com"

if [ "$GIT_COMMITTER_EMAIL" = "$OLD_EMAIL" ]
then
    export GIT_COMMITTER_NAME="$CORRECT_NAME"
